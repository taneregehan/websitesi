<button class="accordion">   Live         </button>
<div class="panel">
  <p>
    Microsoft'un internet kullanıcılarına sunduğu ücretsiz e-posta sağlayıcısı. İnternet tarayıcıları üzerinden kullanılabilmekte olan hotmail'in nispeten kolay bir arayüzü vardır. Çalışmalar tamamlandıktan sonra MSN Hotmail'in yeni sürümü Windows Live Hotmail olarak değiştirildi. 
    2013 yılında Microsoft'un Hotmail üzerine kurduğu Outlook.com ile birleştirilmiştir.Hotmail, 1995 yılında Sabeer Bhatia ve Jack Smith tarafından kuruldu. 
    O dönemde tarayıcı üzerinden kullanılabilinen ilk e-posta programı olan Hotmail 
    4 Temmuz 1996 tarihinde tamamen kullanılabilir hale gelmiştir. 
    Hotmail ismi Jack ve Sabeer yeni servislerine isim bulmaya çalıştıkları sırada HTML harflerini çağrıştırdığı için seçilmiştir. Nitekim servisin orijinal ismi Hotmail'dir.
    Aralık 1997'de 8,5 milyon kullanıcıya sahip olan Hotmail, 400 milyon Amerikan dolarına Microsoft'a satıldı ve MSN şemsiyesine alındı. İki yıl sonra, Şubat 1999'da Hotmail 30.5 milyon üyeye ve 17 farklı dil desteğine sahipti. 2007'de MSN Hotmail adı altında hizmet veren servis adı Windows Live Hotmail olarak değiştirildi. Windows Live Hotmail Microsoft'un Windows Live Grubu tarafından yaklaşık 3 yıldır Windows Live Mail adı altında geliştiriliyor ve deneme sürümü olarak seçilmiş kişilerce kullanılabiliyordu. Windows Live Hotmail, MSN Hotmail'e göre çok daha güvenli, sade ve kullanışlıdır. Son güncellemeler ile birlikte Windows Live Hotmail 5GB Ücretsiz E-Posta kapasitesi sunuyor, Plus seçeneği ise 10GB.
    Hotmail, gerçekte FreeBSD işletim sistemi üzerinde çalışmaktaydı. Yazılımın Windows'a uyarlanmasının zorluğundan dolayı bugün halen FreeBSD üzerinde çalıştığı iddia edilir.

     


</p>
